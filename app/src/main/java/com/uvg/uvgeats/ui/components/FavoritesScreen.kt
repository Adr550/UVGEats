            verticalArrangement = Arrangement.Center,
            horizontalAlignment = Alignment.CenterHorizontally
        ) {
            Text("Tus comidas favoritas apareceran aquí")
            Text("(Funcionalidad en desarrollo)", modifier = Modifier.padding(top = 8.dp))
        }
    }
